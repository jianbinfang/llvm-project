#ifndef LLVM_LIB_TARGET_MATRIX_MATRIXSUBTARGET_H
#define LLVM_LIB_TARGET_MATRIX_MATRIXSUBTARGET_H

#include "MatrixConfig.h"

#include "MatrixFrameLowering.h"
#include "MatrixISelLowering.h"
#include "MatrixInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/MC/MCInstrItineraries.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "MatrixGenSubtargetInfo.inc"

//@1
namespace llvm {
class StringRef;

class MatrixTargetMachine;

class MatrixSubtarget : public MatrixGenSubtargetInfo {
  virtual void anchor();

public:

  bool HasChapterDummy;
  bool HasChapterAll;

  bool hasChapter3_1() const {
    return true;
  }

  bool hasChapter3_2() const {
    return false;
  }

  bool hasChapter3_3() const {
    return false;
  }

  bool hasChapter3_4() const {
    return false;
  }

  bool hasChapter3_5() const {
    return false;
  }

  bool hasChapter4_1() const {
    return false;
  }

  bool hasChapter4_2() const {
    return false;
  }

  bool hasChapter5_1() const {
    return false;
  }

  bool hasChapter6_1() const {
    return false;
  }

  bool hasChapter7_1() const {
    return false;
  }

  bool hasChapter8_1() const {
    return false;
  }

  bool hasChapter8_2() const {
    return false;
  }
  
  bool hasChapter9_1() const {
    return false;
  }

  bool hasChapter9_2() const {
    return false;
  }

  bool hasChapter9_3() const {
    return false;
  }

  bool hasChapter10_1() const {
    return false;
  }

  bool hasChapter11_1() const {
    return false;
  }

  bool hasChapter11_2() const {
    return false;
  }

  bool hasChapter12_1() const {
    return false;
  }

protected:
  enum MatrixArchEnum {
    Matrix32I,
    Matrix32II
  };

  // Matrix architecture version
  MatrixArchEnum MatrixArchVersion;

  // IsLittle - The target is Little Endian
  bool IsLittle;

  bool EnableOverflow;

  // HasCmp - cmp instructions.
  bool HasCmp;

  // HasSlt - slt instructions.
  bool HasSlt;

  InstrItineraryData InstrItins;

  const MatrixTargetMachine &TM;

  Triple TargetTriple;

  const SelectionDAGTargetInfo TSInfo;

  std::unique_ptr<const MatrixInstrInfo> InstrInfo;
  std::unique_ptr<const MatrixFrameLowering> FrameLowering;
  std::unique_ptr<const MatrixTargetLowering> TLInfo;

public:
  bool isPositionIndependent() const;
  const MatrixABIInfo &getABI() const;

  /// This constructor initializes the data members to match that
  /// of the specified triple.
  MatrixSubtarget(const Triple &TT, StringRef CPU, StringRef FS,
                bool little, const MatrixTargetMachine &_TM);

//- Vitual function, must have
  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  bool isLittle() const { return IsLittle; }
  bool hasMatrix32I() const { return MatrixArchVersion >= Matrix32I; }
  bool isMatrix32I() const { return MatrixArchVersion == Matrix32I; }
  bool hasMatrix32II() const { return MatrixArchVersion >= Matrix32II; }
  bool isMatrix32II() const { return MatrixArchVersion == Matrix32II; }

  /// Features related to the presence of specific instructions.
  bool enableOverflow() const { return EnableOverflow; }
  bool disableOverflow() const { return !EnableOverflow; }
  bool hasCmp()   const { return HasCmp; }
  bool hasSlt()   const { return HasSlt; }

  bool abiUsesSoftFloat() const;

  bool enableLongBranchPass() const {
    return hasMatrix32II();
  }
  
  unsigned stackAlignment() const { return 8; }

  MatrixSubtarget &initializeSubtargetDependencies(StringRef CPU, StringRef FS,
                                                 const TargetMachine &TM);

  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
  const MatrixInstrInfo *getInstrInfo() const override { return InstrInfo.get(); }
  const TargetFrameLowering *getFrameLowering() const override {
    return FrameLowering.get();
  }
  const MatrixRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo->getRegisterInfo();
  }
  const MatrixTargetLowering *getTargetLowering() const override {
    return TLInfo.get();
  }
  const InstrItineraryData *getInstrItineraryData() const override {
    return &InstrItins;
  }
};
} // End llvm namespace

#endif // #if CH >= CH3_1

